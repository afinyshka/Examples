void RedrawPicture(int[,] pic, string zero, string one)
{
    for (int rows = 0; rows < pic.GetLength(0); rows++)
    {
        for (int col = 0; col < pic.GetLength(1); col++)
        {
            if (pic[rows, col] == 0) Console.Write(zero);
            else Console.Write(one);
        }
        Console.WriteLine();
    }
}

