void FillPicture(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillPicture(row - 1, col);
        FillPicture(row, col - 1);
        FillPicture(row+1, col);
        FillPicture(row, col + 1);
    }
}

void FillPictureMy(int[,] array, int row, int col)
{
    if (array[row, col] == 0)
    {
        array[row, col] = 1;
        FillPictureMy(array, row - 1, col);
        FillPictureMy(array, row, col - 1);
        FillPictureMy(array, row+1, col);
        FillPictureMy(array, row, col + 1);
    }
}
