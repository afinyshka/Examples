double Factorial(int n)
{
    if (n == 1) return 1;
    else return (Factorial(n - 1) * n);
}
//--------------------------------------------------------------------------------------------------------------+
double Fibo(int n)
{
    if (n == 1 || n == 2) return 1;
    else
    {
        return (Fibo(n - 1) + Fibo(n - 2));
    }
}
